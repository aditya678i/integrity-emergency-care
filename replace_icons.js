function replaceIcons(filepath) {
    let content = fs.readFileSync(filepath, 'utf-8');
    
    // We match <i class="fa-solid fa-..."></i>
    content = content.replace(/<i class="fa-solid [^"]+"[^>]*><\/i>/g, function(match) {
        for (let key in svgs) {
            if (match.includes(key)) {
                let svg = svgs[key];
                if (match.includes('onclick')) {
                    const onclickMatch = match.match(/onclick="[^"]+"/);
                    if (onclickMatch) {
                        svg = svg.replace('<svg ', '<svg ' + onclickMatch[0] + ' style="cursor:pointer;" ');
                    }
                }
                return svg;
            }
        }
        return match;
    });
    
    fs.writeFileSync(filepath, content, 'utf-8');
}

