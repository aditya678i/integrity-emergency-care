def replace_icons(filepath):
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
    
    def replacer(match):
        full_tag = match.group(0)
        for key, svg in svgs.items():
            if key in full_tag:
                if 'onclick' in full_tag:
                    onclick = re.search(r'onclick="[^"]+"', full_tag)
                    if onclick:
                        svg = svg.replace('<svg ', '<svg ' + onclick.group(0) + ' style="cursor:pointer;" ')
                return svg
        return full_tag

    new_content = re.sub(r'<i class="fa-solid [^"]+"[^>]*></i>', replacer, content)
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)

replace_icons('index.html')
